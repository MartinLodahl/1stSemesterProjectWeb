package DataBase;

public class Monster {
    private int id;
    private int type;
    private int roomId;
    private int x;
    private int y;

    public Monster(int id, int type, int roomId, int x, int y) {
        this.id = id;
        this.type = type;
        this.roomId = roomId;
        this.x = x;
        this.y = y;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getRoomId() {
        return roomId;
    }

    public void setRoomId(int roomId) {
        this.roomId = roomId;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
